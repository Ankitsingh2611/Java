import java.util.*;
class leap
{
	public static void main(String args[])
	{
		int year;
		Scanner obj= new Scanner(System.in);
		System.out.println("enter any year");
		year=obj.nextInt();
		if(year%4==0 && year%100!=0|| year%400==0)
		{
			System.out.println("leap year");
		}
		else
		{
			System.out.println("not a leap year");
		}
	}
}
		